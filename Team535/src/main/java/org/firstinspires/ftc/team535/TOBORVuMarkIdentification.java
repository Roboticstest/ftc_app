@Autonomous(name="VuMark", group ="Autonomous")
@Disabled
public class TOBORVuMarkIdentification extends OpMode {

    public static final String TAG = "Vuforia VuMark Sample";

    OpenGLMatrix lastLocation = null;

    /**
     * {@link #vuforia} is the variable we will use to store our instance of the Vuforia
     * localization engine.
     */
    VuforiaLocalizer vuforia;
    VuforiaTrackables relicTrackables;
    VuforiaTrackable relicTemplate;
    RelicRecoveryVuMark vuMark;

    Crypto cryptoLocation;

    public enum Crypto {
        Left,
        Center,
        Right,
        Unknown
    }


    @Override
    public void init() {
        int cameraMonitorViewId = hardwareMap.appContext.getResources().getIdentifier("cameraMonitorViewId", "id", hardwareMap.appContext.getPackageName());
        VuforiaLocalizer.Parameters parameters = new VuforiaLocalizer.Parameters(cameraMonitorViewId);
        parameters.vuforiaLicenseKey = "ATsODcD/////AAAAAVw2lR...d45oGpdljdOh5LuFB9nDNfckoxb8COxKSFX";
        parameters.cameraDirection = VuforiaLocalizer.CameraDirection.BACK;
        this.vuforia = ClassFactory.createVuforiaLocalizer(parameters);
        relicTrackables = this.vuforia.loadTrackablesFromAsset("RelicVuMark");
        relicTemplate = relicTrackables.get(0);
        relicTemplate.setName("relicVuMarkTemplate");
        vuMark = RelicRecoveryVuMark.from(relicTemplate);
        relicTrackables.activate();
        cryptoLocation = Crypto.Unknown;
    }

    @Override
    public void init_loop() {

    }

    @Override
    public void start() {

    }

    @Override
    public void loop() {

        if(vuMark == RelicRecoveryVuMark.LEFT){
            cryptoLocation = Crypto.Left;

        } else if (vuMark == RelicRecoveryVuMark.CENTER) {
            cryptoLocation = Crypto.Center;

        }
        else if (vuMark == RelicRecoveryVuMark.RIGHT){
            cryptoLocation = Crypto.Right;

        }

        if (vuMark != RelicRecoveryVuMark.UNKNOWN) {


            telemetry.addData("VuMark", "%s visible", vuMark);

        } else {
            telemetry.addData("VuMark", "not visible");
        }
telemetry.addData("VuMark", cryptoLocation);


    }


    @Override
    public void stop() {

    }
}